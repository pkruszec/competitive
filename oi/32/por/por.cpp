#include <iostream>
using namespace std;

int main() {
    int n;
    // Read the size of the permutation.
    cin >> n;

    for (int i = 0; i < 10; ++i) {
        // Swap the first two elements.
        cout << "1 2" << endl;

        // Read the number of inversions from the simulator.
        int inversions;
        cin >> inversions;

        if(inversions == 0) {
            break;
        }
    }

    return 0;
}
